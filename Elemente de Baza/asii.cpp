#include <iostream>
using namespace std;
int a, b;
int main()
{
	cin >> a >> b;
	cout << a + b << " " << a - b << " " << a * b << " " << a / b;
}
//we use <<" "<< to put a space between the results. a+b is the sum, a-b is the subtraction, a*b is the multiplication and a/b is the division(the quotient)